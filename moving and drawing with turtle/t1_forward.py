import turtle 
  
# moves the pen in the  
# forward direction by  
# 110 pixels 
turtle.forward(110) 
  
# changes the direction of  
# the pen by 10 degrees in the 
# left direction 
turtle.right(110) 
  
# moves the pen in the  
# forward direction in  
# the new direction by 
# 110 pixels 
turtle.forward(110)